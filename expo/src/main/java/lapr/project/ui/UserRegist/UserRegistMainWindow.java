    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String username = jTextField2.getText();
        String password = jPasswordField1.getText();
        String email = jTextField3.getText();
        String keyword = jTextField4.getText();
        
        int shift = Integer.parseInt(jTextField5.getText());

        try{controller.verifyUsername(username);
        controller.verifyKeyword(keyword);
        controller.verifyPassword(password);
              String pass = controller.cipherPassword(password,shift);
              String nam =controller.ceaserCipher(keyword,name,shift);
              String usernam =controller.ceaserCipher(keyword,username,shift);
              String mail =controller.ceaserCipher(keyword,email,shift);
              User user = controller.createUser(nam,usernam,pass,mail);
              Encryption encryption = controller.createEncryption(user, keyword, shift);
              controller.addUser(user);
              controller.addEncryption(encryption);
             
              
              
        }catch(UserRegistException e){
            e.getMessage();
        
        }
       
        
            
            
         
              
         
        
      
    }//GEN-LAST:event_jButton1ActionPerformed

