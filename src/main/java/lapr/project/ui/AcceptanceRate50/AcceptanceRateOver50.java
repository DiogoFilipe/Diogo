    public AcceptanceRateOver50(FairCenter fc, User u) {
        this.fc = fc;
        this.u = u;
        controller = new AcceptanceRateOver50Controller(fc);
        this.setVisible(true);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                if (JOptionPane.showConfirmDialog(AcceptanceRateOver50.this, "Do you want to close the application?", "WARNING", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                    dispose();
                }
            }
        });
        initComponents();
        model = new DefaultListModel<>();
        for(String e:controller.getEventList()){
            model.addElement(e);
        }
       jList1.setModel(model);
       jScrollPane1.setViewportView(jList1);
       jList1.addListSelectionListener(((ListSelectionEvent e) -> {
           String event= jList1.getSelectedValue();
           boolean acc = controller.getAcceptanceRate(event);
           if(acc==true){
               jTextField1.setText("yes");
           }else if(acc==false){
               jTextField1.setText("no");
           }
       }));
    }


